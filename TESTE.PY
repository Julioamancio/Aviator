# üß© Desafio 2 ‚Äì eh_par(n)
# Objetivo: retornar True se o n√∫mero for par e False
# se for √≠mpar, usando o operador m√≥dulo (%).
# Observa√ß√£o: a fun√ß√£o n√£o imprime nada; a entrada e o
# print ficam fora da fun√ß√£o.
# ----------------------------------------------------

def eh_par(n):  # Define a fun√ß√£o com um par√¢metro num√©rico 'n'.
    return n % 2 == 0  # Retorna True se o resto por 2 for 0; caso contr√°rio, False.

# Entrada do usu√°rio (fora da fun√ß√£o):
entrada = input("Digite um n√∫mero inteiro: ")  # Solicita um n√∫mero inteiro ao usu√°rio.
n = int(entrada)  # Converte a string digitada para inteiro (pode lan√ßar erro se n√£o for n√∫mero).
resultado_par = eh_par(n)  # Chama a fun√ß√£o e guarda o valor booleano retornado.
print(resultado_par)  # Exibe True (par) ou False (√≠mpar) no console.


