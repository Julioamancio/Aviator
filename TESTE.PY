# üß© Desafio 3 ‚Äì calcular_media(a, b, c)
# Objetivo: receber 3 notas e retornar a m√©dia com
# duas casas decimais usando round(media, 2).
# A fun√ß√£o n√£o deve imprimir nada.
# ----------------------------------------------------

def calcular_media(a, b, c):  # Define a fun√ß√£o que recebe tr√™s n√∫meros (notas).
    media = (a + b + c) / 3  # Calcula a m√©dia aritm√©tica das tr√™s notas.
    return round(media, 2)  # Arredonda a m√©dia para 2 casas decimais e retorna.

# Testes do Desafio 3 (fora da fun√ß√£o):
print(calcular_media(7, 8, 9))        # Exemplo 1: deve exibir 8.0
